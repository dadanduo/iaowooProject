public class CustomRoundAngleImageView extends AppCompatImageView {
    float width, height;

    int px=10;

    public CustomRoundAngleImageView(Context context) {
        this(context, null);
        init(context, null);
    }

    public CustomRoundAngleImageView(Context context, AttributeSet attrs) {
        this(context, attrs, 0);
        init(context, attrs);
    }

    public CustomRoundAngleImageView(Context context, AttributeSet attrs, int defStyleAttr) {
        super(context, attrs, defStyleAttr);
        init(context, attrs);
    }

    private void init(Context context, AttributeSet attrs) {
        if (Build.VERSION.SDK_INT < 18) {
            setLayerType(View.LAYER_TYPE_SOFTWARE, null);
        }
    }

    @Override
    protected void onLayout(boolean changed, int left, int top, int right, int bottom) {
        super.onLayout(changed, left, top, right, bottom);
        width = getWidth();
        height = getHeight();
    }

    @Override
    protected void onDraw(Canvas canvas) {
        if (width >= px && height > px) {
            Path path = new Path();
            //四个圆角
            path.moveTo(px, 0);
            //右上角
            path.lineTo(width - px, 0);
            path.quadTo(width, 0, width, px);
            //右下角
            path.lineTo(width, height-12);
            path.quadTo(width, height, width-px, height);
            //左下角
            path.lineTo(px, height);
            path.quadTo(0, height, 0, height-px);
            //左上角
            path.lineTo(0, px);
            path.quadTo(0, 0, px, 0);

            canvas.clipPath(path);
        }
        super.onDraw(canvas);
    }

}
